class BridgeExecutionEvidenceClass(str, Enum):
    DECLARED = "DECLARED"
    STATICALLY_VALIDATED = "STATICALLY_VALIDATED"
    CONTRACT_EXECUTED = "CONTRACT_EXECUTED"
    INTEGRATION_EXECUTED = "INTEGRATION_EXECUTED"
    CANONICAL_RUNTIME_EXECUTED = "CANONICAL_RUNTIME_EXECUTED"
    EXTERNAL_OPERATIONALLY_EXECUTED = "EXTERNAL_OPERATIONALLY_EXECUTED"
    REJECTED = "REJECTED"
    FAILED = "FAILED"
    UNEXECUTED = "UNEXECUTED"
    RETIRED = "RETIRED"
    BLOCKED = "BLOCKED"


